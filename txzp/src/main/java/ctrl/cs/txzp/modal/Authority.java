package ctrl.cs.txzp.modal;

public class Authority {
    private Integer authId;

    private Integer authRoleid;

    private Integer authFunid;

    public Integer getAuthId() {
        return authId;
    }

    public void setAuthId(Integer authId) {
        this.authId = authId;
    }

    public Integer getAuthRoleid() {
        return authRoleid;
    }

    public void setAuthRoleid(Integer authRoleid) {
        this.authRoleid = authRoleid;
    }

    public Integer getAuthFunid() {
        return authFunid;
    }

    public void setAuthFunid(Integer authFunid) {
        this.authFunid = authFunid;
    }
}